public class ch02ex02 {
	public static void main(String[] args) {
			int a = 5, b = 3;
			System.out.println(a==b);
			System.out.println(a>=b);
			System.out.println(a!=b);
			System.out.println(a>b && b==3); //&&그리고
			System.out.println(a<b && b==3);
			System.out.println(a>b || b==3); //|| 또는
			System.out.println(a<b || b==3);
			System.out.println(!(3>5));


			}
}
