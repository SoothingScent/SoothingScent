public class ch02ex07 {
	public static void main(String[] args) {
		int a,b,c,d;
		a=15;
		b=015; // 0  붙여서 8진수 15
		c=0x1f; // 0x 붙여서 16진수 1f
		d=0b10110; // 0b 붙여서 2진수 10110
				System.out.println(a);	//15
				System.out.println(b);   //13
				System.out.println(c);   //31
				System.out.println(d);   //22

	}
}
